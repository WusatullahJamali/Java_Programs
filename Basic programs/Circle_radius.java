public class Circle_radius{
public static void main(String args []){

 float cr=3.35f;
 float cf=2*3.14f*3.35f;
 double diameter1=(3.14*cr*cr);
 float area=3.14f*3.35f;
 float sum_diameter=(cr*area*cr);

 
System.out.println("The radius of circle is " + cr);
System.out.println("The circumference of circle is " + cf);
System.out.println("The diameter of circle is " + diameter1);
System.out.println("The area of circle is " + area);
System.out.println("the total diameter is " + sum_diameter);
}
}


